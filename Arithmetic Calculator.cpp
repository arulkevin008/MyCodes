//Arithmetic Calculator
#include<iostream>
using namespace std;
int main()
{
	int a,b;
	cout<<"Enter the First Number:";
	cin>>a;
	cout<<"Enter the Second Number:";
	cin>>b;
	cout<<"Addition:"<<a+b<<endl;
	cout<<"Subtraction:"<<a-b<<endl;
	cout<<"Multiplication:"<<a*b<<endl;
	cout<<"Divsion(Quotient a/b):"<<a/b<<endl;
	cout<<"Division(Quotient b/a):"<<b/a<<endl;
	cout<<"Modulus(Remainder a%b):"<<a%b<<endl;
	cout<<"Modulus(Remainder b%a)"<<b%a<<endl;
	cout<<"Sqaure of First Number:"<<a*a<<endl;
	cout<<"Square of Second Number:"<<b*b<<endl;
	return 0;
}

